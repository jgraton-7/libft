#include <stdio.h>
#include <ctype.h>

int ft_isalnum(int arg)
{
	if( (48 <= arg && arg >= 57) || (65 <= arg && arg >= 90) ||
	 (97 <= arg && arg >= 122))
	{
		return(1);
	}
	else
	{
		return (0);
	}
}
